#!/usr/bin/env node
// Regenerates seed/products.sql from the client-side catalog
// (apps/web/public/data/seed-products.json) so D1's `product` table carries
// the same rows the web app seeds into its local Dexie DB. Without this,
// equipment.product_id's FK to product.id fails the first time a signed-in
// user syncs equipment linked to a catalog product — see the sync fix this
// script was added for.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";
import { getTableColumns } from "drizzle-orm";
import { product } from "../src/schema.ts";

const here = path.dirname(fileURLToPath(import.meta.url));
const catalogPath = path.join(here, "../../../apps/web/public/data/seed-products.json");
const outPath = path.join(here, "../seed/products.sql");

const catalog = JSON.parse(readFileSync(catalogPath, "utf8"));

function sqlString(value) {
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlValue(value) {
  if (value === undefined || value === null) return "NULL";
  if (typeof value === "object") return sqlString(JSON.stringify(value));
  return sqlString(String(value));
}

// How to pull each seeded product column's value out of a catalog entry.
// Keyed by the schema.ts property name (not the SQL column name) so it lines
// up with getTableColumns(product) below. Every schema column must appear
// either here or in SYNC_ONLY_COLUMNS — see the check below — so a new
// product column can no longer be silently dropped from the generated SQL.
const VALUE_GETTERS = {
  id: (p) => p.id,
  kind: (p) => p.kind,
  brand: (p) => p.brand,
  model: (p) => p.model,
  imageUrl: (p) => p.imageUrl ?? null,
  grindScale: (p) => p.grindScale ?? null,
  methodHint: (p) => p.methodHint ?? null,
  specs: (p) => p.specs ?? null,
  status: (p) => p.status ?? "seed",
  clientId: (p) => p.id,
};

// Sync-only bookkeeping columns that aren't part of the catalog and have
// their own DB defaults/nullability — intentionally not seeded.
const SYNC_ONLY_COLUMNS = new Set(["updatedAt", "deletedAt"]);

// Left untouched on reseed rather than overwritten with catalog defaults:
// status can be promoted by real usage (community/verified), and clientId is
// the row's idempotency key.
const NOT_UPDATED_ON_CONFLICT = new Set(["id", "status", "clientId"]);

const schemaColumns = getTableColumns(product);

for (const key of Object.keys(schemaColumns)) {
  if (!VALUE_GETTERS[key] && !SYNC_ONLY_COLUMNS.has(key)) {
    throw new Error(
      `product.${key} (packages/db/src/schema.ts) has no seed mapping in ` +
        `generate-seed-sql.mjs — add it to VALUE_GETTERS, or to SYNC_ONLY_COLUMNS ` +
        `if it shouldn't be seeded from the catalog.`,
    );
  }
}

const insertColumns = Object.keys(schemaColumns).filter((key) => VALUE_GETTERS[key]);
const updateColumns = insertColumns.filter((key) => !NOT_UPDATED_ON_CONFLICT.has(key));
const sqlName = (key) => schemaColumns[key].name;

const lines = catalog.map((p) => {
  const values = insertColumns.map((key) => sqlValue(VALUE_GETTERS[key](p))).join(", ");
  const updateClause = updateColumns.map((key) => `${sqlName(key)} = excluded.${sqlName(key)}`).join(", ");
  return `INSERT INTO product (${insertColumns.map(sqlName).join(", ")}) VALUES (${values})\nON CONFLICT (id) DO UPDATE SET ${updateClause};`;
});

const header = `-- Generated by packages/db/scripts/generate-seed-sql.mjs from\n-- apps/web/public/data/seed-products.json. Do not edit by hand — rerun the\n-- script instead. Applied via:\n--   wrangler d1 execute kvarn --local --file=../../packages/db/seed/products.sql\n--   wrangler d1 execute kvarn --remote --file=../../packages/db/seed/products.sql\n`;

writeFileSync(outPath, header + "\n" + lines.join("\n\n") + "\n");
console.log(`Wrote ${catalog.length} product rows to ${outPath}`);
