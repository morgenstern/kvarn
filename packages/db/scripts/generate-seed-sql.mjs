#!/usr/bin/env node
// Regenerates seed/products.sql from the client-side catalog
// (apps/web/public/data/seed-products.json) so D1's `product` table carries
// the same rows the web app seeds into its local Dexie DB. Without this,
// equipment.product_id's FK to product.id fails the first time a signed-in
// user syncs equipment linked to a catalog product — see the sync fix this
// script was added for.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const here = path.dirname(fileURLToPath(import.meta.url));
const catalogPath = path.join(here, "../../../apps/web/public/data/seed-products.json");
const outPath = path.join(here, "../seed/products.sql");

const catalog = JSON.parse(readFileSync(catalogPath, "utf8"));

function sqlString(value) {
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlValue(value) {
  if (value === undefined || value === null) return "NULL";
  if (typeof value === "object") return sqlString(JSON.stringify(value));
  return sqlString(String(value));
}

const columns = ["id", "kind", "brand", "model", "image_url", "grind_scale", "specs", "status", "client_id"];

const lines = catalog.map((p) => {
  const values = [p.id, p.kind, p.brand, p.model, p.imageUrl ?? null, p.grindScale ?? null, p.specs ?? null, p.status ?? "seed", p.id]
    .map(sqlValue)
    .join(", ");
  return `INSERT INTO product (${columns.join(", ")}) VALUES (${values})\nON CONFLICT (id) DO UPDATE SET kind = excluded.kind, brand = excluded.brand, model = excluded.model, image_url = excluded.image_url, grind_scale = excluded.grind_scale, specs = excluded.specs;`;
});

const header = `-- Generated by packages/db/scripts/generate-seed-sql.mjs from\n-- apps/web/public/data/seed-products.json. Do not edit by hand — rerun the\n-- script instead. Applied via:\n--   wrangler d1 execute kvarn --local --file=../../packages/db/seed/products.sql\n--   wrangler d1 execute kvarn --remote --file=../../packages/db/seed/products.sql\n`;

writeFileSync(outPath, header + "\n" + lines.join("\n\n") + "\n");
console.log(`Wrote ${catalog.length} product rows to ${outPath}`);
